<h2 class="my-4 text-info text-center">Properties</h2>
<p>Class member variables are called "properties". They are defined by using one of the keywords public, protected, or private, optionally followed by a type declaration, followed by a normal variable declaration. This declaration may include an initialization, but this initialization must be a constant value--that is, it must be able to be evaluated at compile time and must not depend on run-time information in order to be evaluated.</p>
<p>The pseudo-variable $this is available inside any class method when that method is called from within an object context. $this is a reference to the calling object</p>



You might want to take a look at the <a href="https://www.php.net/manual/en/ref.classobj.php" target="_blank" class="btn btn-outline-info">Class/Object Functions.</a>
