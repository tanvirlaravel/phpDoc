    <hr>
    <h3>PHP Superglobals</h3>
    <ul>
        <li><strong>$GLOBALS</strong>- Contains a reference to every variable which is currently available within the global scope of the script. The keys of this array are the names of the global variables.</li>
        <li><strong>$_SERVER </strong>This is an array containing information such as headers, paths, and script locations. The entries in this array are created by the web server. There is no guarantee that every web server will provide any of these.</li>
        <li><strong>$_GET</strong> An associative array of variables passed to the current script via the HTTP GET method.</li>
        <li><strong>$_POST</strong> An associative array of variables passed to the current script via the HTTP POST method.</li>
        <li><strong>$_FILES</strong> An associative array of items uploaded to the current script via the HTTP POST method.</li>
        <li><strong>$_REQUEST</strong> An associative array consisting of the contents of $_GET, $_POST, and $_COOKIE.</li>
        <li><strong>$_COOKIE </strong>An associative array of variables passed to the current script via HTTP cookies.</li>
        <li><strong>$_SESSION </strong>An associative array containing session variables available to the current script.</li>
        <li><strong>$_PHP_SELF </strong>A string containing PHP script file name in which it is called.</li>
        <li><strong>$php_errormsg </strong> $php_errormsg is a variable containing the text of the last error message generated by PHP.</li>        
    </ul>

