<h2 class="my-4 text-info text-center">Predefined Variables</h2>


<p>PHP provides a large number of predefined variables to any script which it runs. </p>

<p>PHP also provides an additional set of predefined arrays containing variables from the web server (if applicable), the environment, and user input. </p>

<p>These arrays are rather special in that they are automatically global - i.e., automatically available in every scope. For this reason, they are often known as "superglobals".</p>


