<h2 class="my-4 text-info text-center">Introduction </h2>
<p>Any PHP script is built out of a series of statements. A statement can be an assignment, a function call, a loop, a conditional statement or even a statement that does nothing (an empty statement). Statements usually end with a semicolon. In addition, statements can be grouped into a statement-group by encapsulating a group of statements with curly braces. A statement-group is a statement by itself as well. The various statement types are described in this chapter.</p>

<p>The following are also considered language constructs even though they are referenced under functions in the manual.</p>

<ul>
    <li>list()</li>
    <li>array()</li>
    <li>echo</li>
    <li>eval()</li>
</ul>


